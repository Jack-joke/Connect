package com.inscripts.keys;

public class JsonParsingKeys {

	/**
	 * <b>ID</b>: Id field in the json
	 */
	public static final String ID = "id";

	/**
	 * <b>N</b>: Buddy Name in buddylist
	 */
	public static final String NAME = "n";

	/**
	 * <b>L</b>: Last name of buddy in buddylist
	 */
	public static final String LINK = "l";

	/**
	 * <b>A</b>: Buddy avatar link
	 */
	public static final String AVATAR_LINK = "a";

	/**
	 * <b>S</b>: Buddy status
	 */
	public static final String STATUS = "s";

	/**
	 * <b>M</b>: Message in one on one chat. It can also be used for getting
	 * announcement message
	 */
	public static final String STATUS_MESSAGE = "m";

	/**
	 * <b>NAME</b>: name of chatroom
	 */
	public static final String CHATROOM_NAME = "name";
	/**
	 * <b>I password of chatroom
	 */
	public static final String CHATROOM_PASSWORD = "i";

	/**
	 * <b>TYPE</b>: Type of chatroom (public,password-protected,invite only)
	 */
	public static final String TYPE = "type";

	/**
	 * <b>ONLINE</b>: No of online users
	 */
	public static final String ONLINE = "online";

	public static final String CREATED_BY = "s";

	public static final String ANNOUNCEMENT_TIME = "t";

	public static final String TEMP_ID = "temp_id";

	public static final String CSCHANNEL = "ch";

}
